/** Convert ISO 2-letter code to flag emoji (works in all modern browsers). */
export function codeToFlag(code) {
  if (!code || code.length !== 2) return "";
  return code
    .toUpperCase()
    .replace(/[A-Z]/g, (c) => String.fromCodePoint(0x1f1e6 + c.charCodeAt(0) - 65));
}

/** Look up a country's display name by ISO code. */
export function getCountryName(code) {
  if (!code) return "";
  const entry = CountryList.find((c) => c.code === code);
  return entry ? entry.name : code;
}

/** Look up a country's continent code by ISO code. */
export function getCountryContinent(code) {
  if (!code) return "";
  const entry = CountryList.find((c) => c.code === code);
  return entry ? entry.continent : "";
}

